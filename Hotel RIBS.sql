create database Hotel_RIBS

default character set utf8
default collate utf8_general_ci;

use Hotel_RIBS;

create table hospede(
id_hospede int not null auto_increment,
nome varchar(50) not null,
cpf varchar(15) not null,
data_nascimento date not null,
genero enum('M', 'F'),
nacionalidade varchar(20) not null,
telefone varchar(15) not null,
email varchar(50),
cidade varchar(50) not null,
cep varchar(15) not null,
rua varchar (50) not null,
numero varchar(10) not null,
PRIMARY KEY(id_hospede) 
)DEFAULT charset=utf8;

CREATE TABLE quarto(
id_quarto int not null,
valor_quarto decimal(5,2) not null,
n_max_pessoas smallint not null,
tipo_quarto enum('simples', 'luxo', 'superluxo'),
qtd_camas enum('1','2','3'),
num_andar smallint not null,
fumante enum('sim', 'não'),
PRIMARY KEY(id_quarto)
)DEFAULT charset=utf8;

CREATE TABLE recepcao(
id_recepcionista int not null auto_increment,
cpf varchar(15) not null,
nome varchar(50) not null,
salario decimal(6,2) not null,
turno enum('matutino', 'vespertino', 'noturno'),
PRIMARY KEY (id_recepcionista)
)DEFAULT charset=utf8;

CREATE TABLE reserva(
id_reserva int not null,
check_in date not null,
check_out date not null,
qtd_diarias smallint not null,
valor_estadia  decimal(6,2) not null,
forma_pagamento enum('debito', 'credito', 'pix', 'dinheiro'),
qtd_criancas smallint not null,
qtd_adulto smallint not null,
turismo enum('sim','não'),
id_hospede int not null,
id_quarto int not null,
id_recepcionista int not null,
PRIMARY KEY (id_reserva),
FOREIGN KEY (id_hospede) REFERENCES hospede (id_hospede),
FOREIGN KEY (id_quarto) REFERENCES quarto (id_quarto),
FOREIGN KEY (id_recepcionista) REFERENCES recepcao (id_recepcionista)
)DEFAULT charset=utf8;

CREATE TABLE spa(
id_spa smallint not null auto_increment,
data_spa date not null,
hr_entrada TIME not null,
hr_saida TIME not null,
id_reserva int not null,
PRIMARY KEY(id_spa),
FOREIGN KEY(id_reserva) REFERENCES reserva (id_reserva)
)DEFAULT charset=utf8;

CREATE TABLE academia(
id_academia smallint not null auto_increment,
data_academia date not null,
hr_entrada TIME not null,
hr_saida TIME not null,
id_reserva int not null,
PRIMARY KEY(id_academia),
FOREIGN KEY(id_reserva) REFERENCES reserva (id_reserva)
)DEFAULT charset=utf8;

CREATE TABLE turismo(
id_turismo smallint not null auto_increment,
qtd_pessoas smallint not null,
data_turismo date not null,
atividades enum('cachoeira','museu','balão','fazendinha'),
id_reserva int not null,
PRIMARY KEY(id_turismo),
FOREIGN KEY(id_reserva) REFERENCES reserva (id_reserva)
)DEFAULT charset=utf8;




